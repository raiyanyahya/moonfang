function textWidth(text, scale) {
  scale = scale || 1;
  let w = 0;
  for (const ch of text) {
    const g = FONT[ch] || FONT[ch.toUpperCase()] || FONT[' '];
    w += (g[0].length + 1) * scale;
  }
  return w > 0 ? w - scale : 0;
}

function drawText(ctx, text, x, y, color, scale) {
  scale = scale || 1;
  ctx.fillStyle = color;
  let cx = Math.round(x);
  const cy = Math.round(y);
  for (const ch of text) {
    const g = FONT[ch] || FONT[ch.toUpperCase()] || FONT[' '];
    for (let r = 0; r < 5; r++) {
      const row = g[r];
      for (let c = 0; c < row.length; c++) {
        if (row[c] === '1') ctx.fillRect(cx + c * scale, cy + r * scale, scale, scale);
      }
    }
    cx += (g[0].length + 1) * scale;
  }
}

// Text with a hard drop shadow, GBA HUD style.
function drawTextShadow(ctx, text, x, y, color, scale, shadow) {
  drawText(ctx, text, x + (scale || 1), y + (scale || 1), shadow || '#0a0812', scale);
  drawText(ctx, text, x, y, color, scale);
}

function drawTextCentered(ctx, text, cx, y, color, scale) {
  drawTextShadow(ctx, text, cx - Math.floor(textWidth(text, scale) / 2), y, color, scale);
}
